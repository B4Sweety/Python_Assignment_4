Multiplication = lambda iNo1, iNo2 : iNo1*iNo2

def main():
    print("Enter first number : ")
    iValue1 = int(input())

    print("Enter second number : ")
    iValue2 = int(input())

    iRet = Multiplication(iValue1, iValue2)

    print("Multiplication is : ",iRet)

if __name__ == "__main__":
    main()

